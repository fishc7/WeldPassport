def main() -> int:
    dry_run = "--dry-run" in sys.argv

    DEST.mkdir(parents=True, exist_ok=True)

    copied = skipped = missing = 0

    for src_str, dst_name in PLAN:
        src = Path(src_str)
        dst = DEST / dst_name

        if not src.exists():
            print(f"  НЕТ ФАЙЛА: {src.name}")
            missing += 1
            continue

        if dst.exists():
            print(f"  пропуск (уже есть): {dst_name}")
            skipped += 1
            continue

        if dry_run:
            print(f"  [dry] скопировать: {dst_name}")
            print(f"        <- {src}")
            copied += 1
            continue

        shutil.copy2(src, dst)
        print(f"  скопировано: {dst_name}")
        copied += 1

    print()
    action = "Будет скопировано" if dry_run else "Скопировано"
    print(f"{action}: {copied}   Пропущено: {skipped}   Не найдено: {missing}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
